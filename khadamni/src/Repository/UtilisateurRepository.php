public function getallIds()
{
    $qb = $this->createQueryBuilder('s')
        ->select('s.id');
    return $qb->getQuery()
    ->getResult();
}

public function findUsers()
    {
        return $this->createQueryBuilder('u')
            ->where('u.role = :role')
            ->setParameter('role', 'user')
            ->orderBy('u.id', 'ASC')
            ->getQuery()
            ->getResult();
    }

    public function findFreelancer()
    {
        return $this->createQueryBuilder('u')
            ->where('u.role = :role')
            ->setParameter('role', 'freelancer')
            ->orderBy('u.id', 'ASC')
            ->getQuery()
            ->getResult();
    }

